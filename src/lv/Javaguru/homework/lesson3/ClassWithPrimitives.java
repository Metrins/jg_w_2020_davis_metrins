package lv.Javaguru.homework.lesson3;

public class ClassWithPrimitives {

    byte biteNumber;
    short shortNumber;
    int intNumber;
    long longNumber;
    char character;
    float floatNumber;
    double doubleNumber;
    boolean aBoolean;

    public void printToConsole() {
        System.out.println("Byte Number is " + biteNumber);
        System.out.println("Short Number is " + shortNumber);
        System.out.println("Integer Number is " + intNumber);
        System.out.println("Long Number is " + longNumber);
        System.out.println("Character is " + character);
        System.out.println("Float Number is " + floatNumber);
        System.out.println("Double Number is " + doubleNumber);
        System.out.println("Boolean is " + aBoolean);



    }

}
